type Props = {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  currentIcon?: string;
  onSelectIcon: (icon: string) => void;
  onRemoveIcon?: () => void;
};

export function IconPicker({
  open,
  onOpenChange,
  currentIcon,
  onSelectIcon,
  onRemoveIcon,
}: Props) {
  const { t } = useTranslation();
  const theme = useTheme();
  const [activeCategory, setActiveCategory] = useState(
    Object.keys(EMOJI_CATEGORIES)[0],
  );

  const categoryKeys = useMemo(() => Object.keys(EMOJI_CATEGORIES), []);
  const currentEmojis = EMOJI_CATEGORIES[activeCategory] ?? [];

  return (
    <Dialog open={open} onOpenChange={onOpenChange} modal>
      <Dialog.Portal>
        <Dialog.Overlay opacity={0.5} />
        <Dialog.Content
          bordered
          elevate
          width={340}
          gap="$2"
          bg="$backgroundHover"
          style={[tw`rounded-3xl overflow-hidden`, { maxHeight: 420 }]}
        >
          <Dialog.Title style={tw`text-center`}>
            {t("Toolbar.addIcon")}
          </Dialog.Title>

          {currentIcon && (
            <View style={tw`flex-row items-center justify-center gap-3 mb-1`}>
              <Text style={tw`text-4xl`}>{currentIcon}</Text>
              {onRemoveIcon && (
                <Pressable
                  onPress={onRemoveIcon}
                  style={{
                    paddingHorizontal: 12,
                    paddingVertical: 5,
                    borderRadius: 8,
                    backgroundColor: "#ef444415",
                  }}
                >
                  <Text style={[tw`text-xs font-medium`, { color: "#ef4444" }]}>
                    {t("Cover.remove")}
                  </Text>
                </Pressable>
              )}
            </View>
          )}

          <View style={tw`flex-row gap-1 px-2`}>
            {categoryKeys.map((key) => (
              <Pressable
                key={key}
                onPress={() => setActiveCategory(key)}
                style={{
                  width: 36,
                  height: 36,
                  alignItems: "center",
                  justifyContent: "center",
                  borderRadius: 8,
                  backgroundColor:
                    activeCategory === key
                      ? theme.backgroundPress.val
                      : "transparent",
                }}
              >
                <Text style={tw`text-lg`}>{key}</Text>
              </Pressable>
            ))}
          </View>

          <View
            style={{
              height: 1,
              backgroundColor: theme.borderColor.val,
              marginHorizontal: 12,
            }}
          />

          <FlatList
            data={currentEmojis}
            numColumns={8}
            keyExtractor={(item) => item}
            contentContainerStyle={tw`px-3 pb-4`}
            columnWrapperStyle={tw`gap-1`}
            renderItem={({ item }) => (
              <Pressable
                onPress={() => onSelectIcon(item)}
                style={{
                  width: 36,
                  height: 36,
                  alignItems: "center",
                  justifyContent: "center",
                  borderRadius: 8,
                }}
              >
                <Text style={tw`text-xl`}>{item}</Text>
              </Pressable>
            )}
          />
        </Dialog.Content>
      </Dialog.Portal>
    </Dialog>
  );
}
